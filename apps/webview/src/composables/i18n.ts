function normalizeLocale(value: string): "en" | "zh-CN" {
  const lower = value.toLowerCase();
  if (lower.startsWith("zh")) {
    return "zh-CN";
  }
  return "en";
}

export function setLocale(nextLocale: string): void {
  locale.value = normalizeLocale(nextLocale);
}

export function getLocale(): string {
  return locale.value;
}

export function t(key: string, params?: Params): string {
  const bundle = messages[locale.value] ?? messages.en ?? {};
  const fallback = messages.en ?? {};
  const template = bundle[key] ?? fallback[key] ?? key;

  return template.replace(/\{(\w+)\}/g, (_, token) => String(params?.[token] ?? `{${token}}`));
}

export function actionLabel(action: string): string {
  const key = `action${action.charAt(0).toUpperCase()}${action.slice(1)}`;
  return t(key);
}
